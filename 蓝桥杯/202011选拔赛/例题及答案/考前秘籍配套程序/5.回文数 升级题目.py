
n = int(input()) # 输入位数
a = 0
# 题目要求4<=n<=7,最多7位数
# 好理解的写法如下：
if n == 4:
    a = 1000
    b = 9999 # 4位数最大到9999，下同
elif n == 5:
    a = 10000
    b = 99999
elif n == 6:
    a = 100000
    b = 999999
elif n == 7:
    a = 100000
    b = 999999
s1 = 0 # 统计回文数
s2 = 0 # 统计回文数中含'99'
for i in range(a,b+1): # 包含a
    c = str(i)
    d = c[::-1] # 字符串翻转
